        template<typename T>
        inline T iceildiv(const T a, const T b) {
            return (a + b - 1) / b;
        }

        template<typename T>
        inline T roundup(const T a, const T b) {
            T rem = a % b;

            if (rem) {
                return a + b - rem;
            } else {
                return a;
            }
        }

